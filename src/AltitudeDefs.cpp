bool AltitudeDefs::parseConfigFile() {
    const QString fileName = configFileLocalPath();
    if (!QFile::exists(fileName)) {
        return false;
    }
    QSettings config(fileName, QSettings::IniFormat);
    qDebug() << config.value("folders/MyFolder", "QQQQ");
    return true;
}

